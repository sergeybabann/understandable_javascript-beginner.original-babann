// конструкция switch case

// Jonh
let name = 'John'

switch (name) {
  case 'David':
    console.log('This is David')
    break
  case 'John':
    console.log('This is John')
    break
  default:
    console.log('Unknown name')
}

// count
let count = 12

switch (count) {
  case 12:
    console.log('count: 12')
    break
  case 15:
    console.log('count: 15')
    break
  default:
    console.log('Unknown count')
}

// number
let number = 10

switch (number * 3) {
  case 30:
    console.log('number: 30')
    break
  case 40:
    console.log('number: 40')
    break
  default:
    console.log('Unknown number')
}
